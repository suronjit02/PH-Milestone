
//  various Math function

console.log(Math.min(34, 54, 66, 54, 76, 33, 234)); // min value in array
console.log(Math.max(34, 54, 66, 54, 76, 33, 234)); // max value in array

console.log(Math.PI); // value of pi
console.log(Math.abs(-10)); // its return a absolute value. (no negative or positive)
console.log(Math.round(4.7)); // give the nearest integer value
console.log(Math.round(4.2)); // give the nearest integer value
console.log(Math.floor(6.99)); // cut the float value
console.log(Math.ceil(8.0001)); // give next nearest integer value
console.log(Math.random()); // random value 0 to 1
console.log(Math.random() * 10); // random value 0 to 10

console.log(Math.round(Math.random() * 100)); // random integer value 0 to 100


