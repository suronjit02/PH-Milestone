
// index idea

let numbers = [34, 54, 54, 65, 87, 24, 87, 90];



// get element by index

console.log(numbers); //full array
console.log(numbers[0]); // 0 index mean the 1st number
console.log(numbers[2]); // 2 index mean the 3rd number
console.log(numbers[4]); // 4 index mean the 5th number


const fifth = numbers[5];

console.log(fifth);


// set or update element by index

numbers[2] = 453;

console.log(numbers);

